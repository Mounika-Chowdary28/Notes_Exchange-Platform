export function normalizeNote(raw) {
  const comments = (raw.comments || []).map((c) => ({
    ...c,
    replies: c.replies || [],
    helpful: c.helpful ?? 0,
    bestAnswer: c.bestAnswer ?? false,
  }))
  return {
    ...DEFAULT_NOTE,
    ...raw,
    tags: raw.tags?.length ? raw.tags : DEFAULT_NOTE.tags,
    versions: raw.versions?.length ? raw.versions : [{ v: raw.version || 1, label: raw.versionLabel || 'v1', at: raw.createdAt }],
    comments,
    uploadedBy: { ...raw.uploadedBy },
  }
}

export function cloneNotes() {
  return MOCK_NOTES_RAW.map((n) => normalizeNote(n))
}

